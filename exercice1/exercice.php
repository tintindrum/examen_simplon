    <p>WampServeur est une plateforme de dev Web qui permet de faire fonctionner localement des scripts PHP , 
Il y'a 3 serveur ( Apache , MySQL et MariaDB ) ,
Un interpréteur pour PHP ainsi que PHPMyAdmin pour interface visuel de l'admninistration Web des requêtes SQL.

Il existe plusieurs pack pour Windows , Mac , Linux :
Mamp (Pour Mac)
Wamp (Pour Windows)
Xamp (Pour Linux)

Tous les composants peuvent sont situés sur une même machine , Les rôles des composants sont : 

Apache est le serveur web "frontal" : il est devant tous les autres et répond aux requêtes du client 
Le languague PHP servira de logique 
MySQL stocke toutes les données 
L'OS assure l'attribution des ressources à ces 3 composants </p><br>

