    <h3>1/ Client / Serveur ( rapport et interaction entre les deux ) ?</h3>
    <p>Le rapport client /serveur désigne un mode d'échange entre eux (avec un réseau local ou serveur distant) avec plusieurs languagues de programmation .

Le client établit la connexion au serveur au serveur à destination d'un ou plusieurs serveur , lorsque la connexion est acceptée par le serveur , il communique 
renvoie une réponse au client . 

Le serveur attend une connexion et à la suite du processus il communique avec le client .</p><br>

    <h3>2/ Différences entre Serveur Local et Serveur Distant ?</h3>
    <p>Un serveur local vous donne l'acces aux données et structures dans un ensemble de dossiers , quand on est en local , on peut modifier creer ou parcourir sur le serveur local 

Un serveur distant donnent accès à des données dans l'entreprise , le niveau d'accès d'un utilisateur dépend de sa sécurité , on affecte souvent un ID client pour admninistrer ,
Ainsi , un utilisateur peut mettre à jour les données sur le serveur distant </p><br>

